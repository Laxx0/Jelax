-----------------------------------------------------------
-- START for one way tiles (see below)---------------------
-----------------------------------------------------------
function getTileRange(tw,th,x,y,w,h)
	gx,gy   = math.floor(x/tw)+1,math.floor(y/th)+1
	gx2,gy2 = math.ceil( (x+w)/tw ),math.ceil( (y+h)/th )
	return gx,gy,gx2,gy2
end
-----------------------------------------------------------
-- END for one way tiles (see below)-----------------------
-----------------------------------------------------------

-----------------------------------------------------------
-- START draw map------------------------------------------
-----------------------------------------------------------
function map_draw()
	local tw,th = grid.tileWidth,grid.tileHeight
	for ty,z in ipairs(grid) do
		ty = ty-1
		for tx,tile in ipairs(z) do
				tx = tx-1
				love.graphics.setColor(tileset[tile].color)
				if tile == 5 or tile == 6 then
					-- draw slopes
					love.graphics.polygon('fill',tx*tw,(ty+1)*th,(tx+1)*tw,(ty+1)*th,(tx+1)*tw,(ty*th))
				else
					-- draw regular blocks
					love.graphics.rectangle('fill',tx*tw,ty*th,tw,th)
				end
		end
	end
end
